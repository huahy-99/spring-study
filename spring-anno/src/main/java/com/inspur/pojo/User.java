package com.inspur.pojo;

import org.springframework.stereotype.Component;

//@Component 这个bean被spring托管
@Component
public class User {

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "name='" + name + '\'' +
                '}';
    }
}
